function soma(n1=0, n2=0){ // Cria a função de soma, aplicando os parâmetros de n1 e n2, o 0 é um parâmetro opcional
    //Para caso o valor n1 ou n2 não seja declarado na chamada  da função
    return n1 + n2;
}
console.log(soma(2, 5)) // Chamada dos parâmetros da função, aplicando 2 pro n1, e 5 pro n2